<h3>欢迎使用支部风采后台管理系统</h3>
<h3>使用前请仔细阅读以下几点：</h3>
<ol>
    <li>重设密码在权限管理中</li>
    <li>在首页设置中设置首页的轮播，最多6个</li>
    <li>在首页设置中 “报名表下载链接” 与 “活动通知下载链接” 已经在此系统部署时设置完成。<span style="color: red">注意：如非必要，请勿填写！</span></li>
    <li><b>查看已提交、驳回、通过的作品可以通过作品列表右上方的按钮来选择切换</b></li>
    <li>评论管理在作品详情页面中</li>
    <li>在院校管理中可添加或删除院校</li>
    <li>在权限管理中可以授权用户是否可以提交成功或访问后台</li>
    <li>在信息审核中可以审核申报的支部或成果，<span style="color: red;">注意：如果删除申请将直接删除所提交的数据，该操作不可逆！</span></li>
</ol>
<p>祝您使用愉快</p>